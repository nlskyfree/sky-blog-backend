package com.skyfree.entity;

public class Github {
    private String htmlUrl;
    private String name;
    private String fork;
    private String forks;
    private String isPrivate;
    private String forksCount;
    private String description;
    private String openIssuesCount;
    private String stargazersCount;
    private String createdAt;
    private String language;
    public String getHtmlUrl() {
        return htmlUrl;
    }
    public void setHtmlUrl(String htmlUrl) {
        this.htmlUrl = htmlUrl;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getFork() {
        return fork;
    }
    public void setFork(String fork) {
        this.fork = fork;
    }
    public String getForks() {
        return forks;
    }
    public void setForks(String forks) {
        this.forks = forks;
    }
    public String getIsPrivate() {
        return isPrivate;
    }
    public void setIsPrivate(String isPrivate) {
        this.isPrivate = isPrivate;
    }
    public String getForksCount() {
        return forksCount;
    }
    public void setForksCount(String forksCount) {
        this.forksCount = forksCount;
    }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }
    public String getOpenIssuesCount() {
        return openIssuesCount;
    }
    public void setOpenIssuesCount(String openIssuesCount) {
        this.openIssuesCount = openIssuesCount;
    }
    public String getStargazersCount() {
        return stargazersCount;
    }
    public void setStargazersCount(String stargazersCount) {
        this.stargazersCount = stargazersCount;
    }
    public String getCreatedAt() {
        return createdAt;
    }
    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }
    public String getLanguage() {
        return language;
    }
    public void setLanguage(String language) {
        this.language = language;
    }
}
